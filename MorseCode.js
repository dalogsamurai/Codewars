function decodeMorse(morseCode) {
    var ref = {
        '.-': 'a',
        '-...': 'b',
        '-.-.': 'c',
        '-..': 'D',
        '.': 'E',
        '..-.': 'f',
        '--.': 'g',
        '....': 'H',
        '..': 'i',
        '.---': 'J',
        '-.-': 'k',
        '.-..': 'l',
        '--': 'm',
        '-.': 'n',
        '---': 'o',
        '.--.': 'p',
        '--.-': 'q',
        '.-.': 'r',
        '...': 's',
        '-': 't',
        '..-': 'U',
        '...-': 'v',
        '.--': 'w',
        '-..-': 'x',
        '-.--': 'Y',
        '--..': 'z',
        '.----': '1',
        '..---': '2',
        '...--': '3',
        '....-': '4',
        '.....': '5',
        '-....': '6',
        '--...': '7',
        '---..': '8',
        '----.': '9',
        '-----': '0',
    };
    return morseCode.split(' ').map(a => a.split(' ').map(b => ref[b]).join('')).join(' ');
}